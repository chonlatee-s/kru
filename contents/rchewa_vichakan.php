                    <h5>สอศ.ร่วมสัมมนาวิชาการ “อาชีวะ 4.0 ศักยภาพแห่งอนาคต”</h5>
                    <p style="font-size:14px; font-weight:300">วันนี้ (14 กรกฎาคม 2561) ดร.สุเทพ ชิตยวงษ์ เลขาธิการคณะกรรมการการอาชีวศึกษา เข้าร่วมกิจกรรมการสัมมนาวิชาการ “อาชีวะ 4.0 ศักยภาพแห่งอนาคต”  จัดขึ้นโดยสถาบันคีนันแห่งเอเชีย ในงาน Chevron Enjoy Science : สนุกวิทย์ พลังคิด เพื่ออนาคต โดยมี พลอากาศเอก ประจิน จั่นตอง รองนายกรัฐมนตรี และรัฐมนตรีว่าการกระทรวงยุติธรรม เป็นประธาน และบรรยายพิเศษ ณ โรงแรมเซ็นทรา บายเซ็นทารา ศูนย์ราชการและคอนเวนชันเซ็นเตอร์ แจ้งวัฒนะ กรุงเทพฯ
                    </p>
                    <img src="../img/rchewa3.jpg" alt="สอบครูผู้ช่วย" class="img-thumbnail mx-auto d-block" style="width:400px;"><p></p>
                    <p style="font-size:14px; font-weight:300">ดร.สุเทพ ชิตยวงษ์ เลขาธิการคณะกรรมการการอาชีวศึกษา กล่าวถึงการเข้าร่วมการสัมมนาวิชาการในครั้งนี้ว่า พลอากาศเอก ประจิน จั่นตอง รองนายกรัฐมนตรี และรัฐมนตรีว่าการกระทรวงยุติธรรม ได้บรรยายพิเศษถึงการเตรียมความพร้อมต่อความท้าทายที่ประเทศไทยกำลังก้าวเข้าสู่ยุคอุตสาหกรรม 4.0 ที่ภาครัฐ ภาคการศึกษา และภาคเอชน จะต้องร่วมมือกันเพื่อตอบรับการผลิตกำลังคนให้มีทักษะที่จำเป็น เป็นที่ต้องการของอุตสาหกรรม 4.0 โดยเฉพาะกำลังคนในสายอาชีวะ ที่จะเป็นกำลังสำคัญของประเทศโดยขณะที่ภาคการผลิตจะก้าวเข้าสู่การผลิตแบบใช้ออโตเมชั่น และขับเคลื่อนด้วยเทคโนโลยีอย่างเต็มรูปแบบ
                    <p>
                    <p style="font-size:14px; font-weight:300">เลขาธิการคณะกรรมการการอาชีวศึกษา กล่าวต่อไปว่า ในส่วนสำนักงานคณะกรรมการการอาชีวศึกษา ซึ่งมีหน้าที่หลักในการผลิตและพัฒนากำลังคนวิชาชีพ ได้มีการขับเคลื่อนให้มีการดำเนินการตามแผนยุทธศาสตร์การศึกษาด้านการผลิตและพัฒนากำลังคน ของกระทรวงศึกษาธิการ ในด้านการเตรียมความพร้อม เพื่อรองรับการพัฒนาทักษะด้านอาชีพที่มีความจำเป็นต่อการเข้าสู่ยุคไทยแลนด์และอุตสาหกรรม 4.0 ในทุกมิติ ทั้งในเป้าหมายระยะสั้น โดยมีโครงการฝึกทักษะวิชาชีพฐานสมรรถนะ (Education to Employment Vocational Boot Camp : E to E) สนับสนุนให้สถานศึกษาจัดหลักสูตรระยะสั้นในการพัฒนาฝีมือ และความเชี่ยวชาญ เพื่อให้แรงงาน และนักศึกษาที่ใกล้จะจบการศึกษามีทักษะความรู้ที่ตรงกับความต้องการของผู้ประกอบการในยุคประเทศไทย 4.0  รวมถึงการดำเนินการในเป้าหมายเชิงพื้นที่ ในการผลิตและพัฒนากำลังคนเพื่อรองรับโครงการพัฒนาระเบียงเศรษฐกิจพิเศษภาคตะวันออก  (Eastern Economic Corridor: EEC) และพื้นที่เศรษฐกิจพิเศษในทุกภาค ตามความต้องการกำลังคนในแต่ละภาคอุตสาหกรรมและธุรกิจ  โดยใช้เทคโนโลยีในการบริหารจัดการข้อมูลแบบ Big Data เพื่อให้ทราบถึงทิศทาง และจำนวนกำลังคนอาชีวศึกษาที่จำเป็น และเหมาะสมในการรองรับภาคอุตสาหกรรมตามที่ต้องการ โดยประสานความร่วมมือจากหน่วยงานทุกภาคส่วนทั้งของรัฐ และเอกชน ตลอดจนภาคธุรกิจและบริการ ภายใต้รูปแบบ “รัฐร่วมเอกชน” หรือสานพลังประชารัฐ สำหรับการพัฒนาคนให้มีความสามารถ ทักษะ และคุณลักษณะที่จำเป็นและเป็นที่ต้องการของยุคอุตสาหกรรม 4.0
                    </p>
                    <p style="font-size:14px; font-weight:300">พร้อมกันนี้หัวข้อการสัมมนาวิชาการ “อาชีวะ 4.0 ศักยภาพแห่งอนาคต”  ซึ่งจัดโดยสถาบันคีนันแห่งเอเชีย โครงการ Chevron Enjoy Science : สนุกวิทย์ พลังคิด เพื่ออนาคต ยังได้จัดให้มีการเสวนา “ช่างเทคนิคยุคใหม่ รู้เท่าทันเทรนด์อุตสาหกรรม4.0 และ “การฝึกอบรมเชิงปฏิบัติการ ห้องเรียน 4.0”แก่ครูผู้สอนอาชีวะเพื่อการแลกเปลี่ยนเรียนรู้เปิดประสบการณ์ใหม่ ในหัวข้อ “ห้องเรียนยุคใหม่ สไตล์แอคทีฟ” เชื่อมโยงองค์ความรู้กับชีวิตประจำวัน และทักษะการทำงาน เทคนิคการสอนใหม่ ๆ  “แมคคาทรอนิกส์ 101” ซึ่งเป็นหลักสูตรที่เหมาะกับครูอาชีวะทุกสาขาที่สามารถนำไปปรับใช้และพัฒนาสื่อการสอนให้กับนักเรียน  และ “การเชื่อมโยงภาคธุรกิจและระบบลีน” (School-Industry Linkage & Lean Manufacturing) แนวคิดการจัดการที่ถูกออกแบบมาเพื่อกำจัดความสูญเสียในกระบวนการต่างๆ อันเป็นที่ยอมรับกันทั่วโลก
                    </p>

