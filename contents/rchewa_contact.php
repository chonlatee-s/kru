                    <p style="font-size:14px; font-weight:300">ดร.สุเทพ ชิตยวงษ์ เลขาธิการคณะกรรมการการอาชีวศึกษา กล่าวว่า สำนักงานคณะกรรมการการอาชีวศึกษา (สอศ.) มีนโยบายในการพัฒนาสถานศึกษาให้มีมาตรฐานในระดับสากล โดยใช้ต้นแบบจากประเทศเยอรมนี    ซึ่งเป็นประเทศที่ได้รับการยอมรับด้านการจัดการอาชีวศึกษา เพื่อเป็นแนวทางในการพัฒนาสถานศึกษาอาชีวศึกษาของไทย และกำหนดให้เป็นต้นแบบในการพัฒนาการจัดการอาชีวศึกษาระบบทวิภาคี โดยมีวัตถุประสงค์ในการเพิ่มขีดความสามารถของกำลังคนอาชีวศึกษาของประเทศไทย ให้มีทักษะทางวิชาชีพที่ตรงตามความต้องการของสถานประกอบการ พัฒนาให้มีความรู้ ความเชี่ยวชาญ และมีทักษะทางวิชาชีพที่สูงขึ้น ตลอดจนสามารถใช้เทคโนโลยีสมัยใหม่ให้สอดคล้องกับวิชาชีพของตนเอง และสอดรับกับนโยบายในการพัฒนาเศรษฐกิจของประเทศไทยในปัจจุบัน (Thailand 4.0)
